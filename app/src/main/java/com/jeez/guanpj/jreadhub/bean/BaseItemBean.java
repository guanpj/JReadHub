package com.jeez.guanpj.jreadhub.bean;

public class BaseItemBean {
    String id;
    String title;

    public BaseItemBean(String id, String title) {
        this.id = id;
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
